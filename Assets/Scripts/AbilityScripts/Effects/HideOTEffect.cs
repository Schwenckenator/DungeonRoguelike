using System.Collections;
using System.Collections.Generic;
using UnityEngine;

[CreateAssetMenu(fileName = "New HideOverTimeEffect", menuName = "Effect/Hide OverTime Effect", order = 52)]
public class HideOTEffect : Effect {


    public GameObject hideOverTime;

        // Overtime Effect
    

    public override void TriggerEffect(Entity target, int minValue, int maxValue) {

        GameObject hideOverTimeObject =  Instantiate(hideOverTime, target.transform);
        target.Stats.AddOvertimeEffect(hideOverTimeObject);
        //TODO Clean this up
        hideOverTimeObject.GetComponent<HideOverTime>().ActivateHideOverTime(target);
    }


}
